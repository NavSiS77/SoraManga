function getJson(url) {
  return new Promise((resolve, reject) => {
    https
      .get(
        url,
        {
          headers: {
            'User-Agent': 'SoraMangaDiploma/1.0 (educational project)'
          }
        },
        (res) => {
          let data = ''
          res.on('data', (c) => {
            data += c
          })
          res.on('end', () => {
            try {
              resolve(JSON.parse(data))
            } catch (e) {
              reject(e)
            }
          })
        }
      )
      .on('error', reject)
  })
}

const sleep = (ms) => new Promise((r) => setTimeout(r, ms))

const rows = []
for (let i = 0; i < titles.length; i++) {
  const q = encodeURIComponent(titles[i])
  const list = await getJson(`https://shikimori.one/api/mangas?search=${q}&limit=5`)
  const best = Array.isArray(list) ? list[0] : null
  rows.push({
    id: i + 1,
    title: titles[i],
    score: best ? parseFloat(String(best.score).replace(',', '.')) || 0 : 0,
    shikiName: best?.name,
    shikiRussian: best?.russian
  })
  process.stderr.write(`${i + 1}/${titles.length}\n`)
  await sleep(400)
}

