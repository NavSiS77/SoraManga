export function getShikimoriScore(mangaId, fallbackRating) {
  const v = SHIKIMORI_SCORE_BY_ID[mangaId]
  if (v != null && !Number.isNaN(v)) {
    return Math.min(10, Math.max(1, Number(v)))
  }
  return Math.min(10, Math.max(1, Number(fallbackRating) || 8))
}

export function getShikimoriPopularityRank(mangaId) {
  const r = SHIKIMORI_POPULARITY_RANK_BY_ID[mangaId]
  if (r != null) {
    return r
  }
  return 500 + Number(mangaId)
}
